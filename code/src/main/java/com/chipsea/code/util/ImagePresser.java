    /**
     * Save image to the SD card
     *
     * @param photoBitmap
     * @param photoName
     * @param path
     */
    public static void savePhotoToSDCard(Bitmap photoBitmap, String path,
                                         String photoName) {
        if (checkSDCardAvailable()) {
            File dir = new File(path);
            if (!dir.exists()) {
                dir.mkdirs();
            }

            File photoFile = new File(path, photoName);
            if (photoFile.exists()) {
                photoFile.delete();
            }
            photoFile = new File(path, photoName);
            FileOutputStream fileOutputStream = null;
            try {
                fileOutputStream = new FileOutputStream(photoFile);
                if (photoBitmap != null) {
                    if (photoName.endsWith(".jpg")) {
                        if (photoBitmap.compress(Bitmap.CompressFormat.JPEG,
                                100, fileOutputStream)) {
                            fileOutputStream.flush();
                            // fileOutputStream.close();
                        }
                    } else if (photoName.endsWith(".png")) {
                        if (photoBitmap.compress(Bitmap.CompressFormat.PNG,
                                100, fileOutputStream)) {
                            fileOutputStream.flush();
                            // fileOutputStream.close();
                        }
                    }
                }
            } catch (FileNotFoundException e) {
                photoFile.delete();
                e.printStackTrace();
            } catch (IOException e) {
                photoFile.delete();
                e.printStackTrace();
            } finally {
                try {
                    fileOutputStream.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
    }

